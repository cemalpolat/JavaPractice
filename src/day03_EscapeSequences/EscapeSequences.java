package day03_EscapeSequences;
/*
escape sequences : MUST be given with double quote
    \n: starts a new line
    \t: tab
    \\: single back slash, in order to give one backslash
    \": double quote

 */
public class EscapeSequences {

    public static void main(String[] args) {
        System.out.println("Java \nPython \nC#");
        System.out.println("\nSentence-1 \nSentence-2 \nSentence-3 \nSentence-4");
        System.out.println("------------------------------------------------");
        System.out.println("\tJava Is cool programming language");
        System.out.println("----------------------------------------");
        System.out.println("/ \\");
        System.out.println("----------------------------------------");
        System.out.println("May favorite Tv Show is \"Guldur Guldur\"");

    }




}
